package model;

public class Friend{
	private String contactNumber;
	private String name;
	
	
	public Friend(String contactNumber, String name) {
		this.contactNumber = contactNumber;
		this.name = name;
	}

	public String getContactNumber() {
		return contactNumber;
	}
	
	public String getName() {
		return name;
	}
	
	
}
